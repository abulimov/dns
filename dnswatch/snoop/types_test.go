func TestRawDecoderByType(t *testing.T) {
	d, err := RawDecoderByType("dns")
	require.Nil(t, err)
	require.NotNil(t, d)

	d, err = RawDecoderByType("random")
	require.Nil(t, d)
	require.NotNil(t, err)
}

func TestDNSSrcPort(t *testing.T) {
	d, err := RawDecoderByType("dns")
	require.Nil(t, err)

	dp, err := d.SrcPort()
	require.NotNil(t, err)
	require.Equal(t, 0, dp)

	err = d.Unmarshal(dnsRawQueryIPv6)
	require.Nil(t, err)

	dp, err = d.SrcPort()
	require.Nil(t, err)
	require.Equal(t, 47633, dp)
}

func TestDNSDstPort(t *testing.T) {
	d, err := RawDecoderByType("dns")
	require.Nil(t, err)
	require.NotNil(t, d)

	dp, err := d.DstPort()
	require.NotNil(t, err)
	require.Equal(t, 0, dp)

	err = d.Unmarshal(dnsRawQueryIPv6)
	require.Nil(t, err)

	dp, err = d.DstPort()
	require.Nil(t, err)
	require.Equal(t, 53, dp)
}

func TestDNSHeader(t *testing.T) {
	d, err := RawDecoderByType("dns")
	require.Nil(t, err)

	require.Equal(t, []string{"DNS Qry Name", "DNS Resp IP"}, d.Header())
}

func TestDNSRow(t *testing.T) {
	d, err := RawDecoderByType("dns")
	require.Nil(t, err)

	err = d.Unmarshal(dnsRawResponseIPv4)
	require.Nil(t, err)

	row, err := d.Row()
	require.Equal(t, []string{"github.com", "140.82.121.3"}, row)
	require.Nil(t, err)
}

func TestDNSValid(t *testing.T) {
	d, err := RawDecoderByType("dns")
	require.Nil(t, err)

	valid := d.Valid()
	require.Equal(t, false, valid)

	err = d.Unmarshal(dnsRawResponseIPv4)
	require.Nil(t, err)

	valid = d.Valid()
	require.Equal(t, true, valid)
}

func TestDNSUnmarshal(t *testing.T) {
	d, err := RawDecoderByType("dns")
	require.Nil(t, err)

	err = d.Unmarshal(dnsRawQueryIPv4)
	require.Nil(t, err)
	require.Equal(t, layers.LayerTypeEthernet, d.(*DNSDecoder).layers[0])
	require.Equal(t, uint16(0x800), uint16(d.(*DNSDecoder).eth.EthernetType))
	require.Equal(t, layers.LayerTypeIPv4, d.(*DNSDecoder).layers[1])
	require.Equal(t, "192.168.0.207", d.(*DNSDecoder).ip4.SrcIP.String())
	require.Equal(t, "192.168.0.1", d.(*DNSDecoder).ip4.DstIP.String())
	require.Equal(t, layers.LayerTypeUDP, d.(*DNSDecoder).layers[2])
	require.Equal(t, "45566", d.(*DNSDecoder).udp.SrcPort.String())
	require.Equal(t, "53(domain)", d.(*DNSDecoder).udp.DstPort.String())
	require.Equal(t, layers.LayerTypeDNS, d.(*DNSDecoder).layers[3])
	require.Equal(t, false, d.(*DNSDecoder).dns.QR)
	require.Equal(t, "github.com", string(d.(*DNSDecoder).dns.Questions[0].Name))

	err = d.Unmarshal(dnsRawResponseIPv4)
	require.Nil(t, err)
	require.Equal(t, layers.LayerTypeEthernet, d.(*DNSDecoder).layers[0])
	require.Equal(t, uint16(0x800), uint16(d.(*DNSDecoder).eth.EthernetType))
	require.Equal(t, layers.LayerTypeIPv4, d.(*DNSDecoder).layers[1])
	require.Equal(t, "192.168.0.1", d.(*DNSDecoder).ip4.SrcIP.String())
	require.Equal(t, "192.168.0.207", d.(*DNSDecoder).ip4.DstIP.String())
	require.Equal(t, layers.LayerTypeUDP, d.(*DNSDecoder).layers[2])
	require.Equal(t, "53(domain)", d.(*DNSDecoder).udp.SrcPort.String())
	require.Equal(t, "45566", d.(*DNSDecoder).udp.DstPort.String())
	require.Equal(t, layers.LayerTypeDNS, d.(*DNSDecoder).layers[3])
	require.Equal(t, true, d.(*DNSDecoder).dns.QR)
	require.Equal(t, "github.com", string(d.(*DNSDecoder).dns.Questions[0].Name))
	require.Equal(t, "github.com", string(d.(*DNSDecoder).dns.Answers[0].Name))
	require.Equal(t, "140.82.121.3", d.(*DNSDecoder).dns.Answers[0].IP.String())
	require.Nil(t, err)
}
